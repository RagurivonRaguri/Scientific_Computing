# int, float, complex, list, tuple, dict, set, and bool
def complex_function():
    x=2
    y=5
    complex_number = complex(x,y)
    print(complex_number)
    print(type(complex_number))

def int_function():
    whole_number = 1
    print(whole_number)
    print(type(whole_number))

def float_function():
    float = 1.23
    print(float)
    print(type(float))

def list_function():
    my_list = list([1,2,3,4,5])
    print(my_list)
    print(type(my_list))

def tuple_function():
    my_tuple = tuple(["Spike", "Tom", "Jerry"])
    print(my_tuple)
    print(type(my_tuple))

def set_function():
    my_set = set({1,2,3,4,5})
    print(my_set)
    print(type(my_set))

def dict_function():
    my_dict = dict({"Kenya": "Nairobi","Tanzania": "Dodoma","Uganda": "Kampala"})
    print(my_dict)
    print(type(my_dict))

